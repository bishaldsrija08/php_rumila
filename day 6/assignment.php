<?php
// Assignment Operators
$a = 10;
echo "Initial value: " . $a . "<br>"; // 10
$a += 5; // $a = $a + 5
echo "After addition: " . $a . "<br>"; // 15

$b = 5;
echo "Initial value: " . $b . "<br>"; // 5
$b -= 2; // $b = $b - 2
echo "After subtraction: " . $b . "<br>"; // 3

$c = 10;
echo "Initial value: " . $c . "<br>"; // 10
$c *= 2; // $c = $c * 2
echo "After multiplication: " . $c . "<br>"; // 20

$d = 10;
echo "Initial value: " . $d . "<br>"; // 10
$d /= 2; // $d = $d / 2
echo "After division: " . $d . "<br>"; // 5

$e = 10;
echo "Initial value: " . $e . "<br>"; // 10
$e %= 3; // $e = $e % 3
echo "After modulus: " . $e . "<br>"; // 1

?>